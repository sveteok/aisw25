let n = 10;

console.log("n: " + n);

n += 5;
console.log("After adding 5 to n: " + n);

n *= 2;
console.log("After multiply n with 2: " + n);

n -= 5;
console.log("After subtracting 5 from n: " + n);

n /= 2;
console.log("After dividing n with 2: " + n);
